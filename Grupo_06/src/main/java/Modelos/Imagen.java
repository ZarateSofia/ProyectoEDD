public class Imagen {
    String ruta;
    int width;
    int height;

    public Imagen(String ruta, int width, int height) {
        this.ruta = ruta;
        this.width = width;
        this.height = height;
    }

    public String getRuta() {
        return ruta;
    }

    public void setRuta(String ruta) {
        this.ruta = ruta;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
    
    
    
}
